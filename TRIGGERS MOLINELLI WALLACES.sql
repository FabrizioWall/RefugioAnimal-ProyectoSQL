DELIMITER ;

DELIMITER //
CREATE FUNCTION `hacer_totales`(producto_id INT) 
RETURNS FLOAT
READS SQL DATA
BEGIN
	DECLARE total FLOAT;
    
    SELECT SUM(precio_unitario + monto_impositivo) INTO total
	FROM compras
	WHERE id_producto = producto_id;

	RETURN total;
END;
//
DELIMITER ;

DELIMITER //
CREATE FUNCTION `hacer_totales_medicamentos`(medicamento_id INT) 
RETURNS FLOAT
READS SQL DATA
BEGIN
	DECLARE total FLOAT;
    
	SELECT SUM(precio_unitario + monto_impositivo) INTO total
    FROM medicamentos
    WHERE id_medicamento = medicamento_id;
    
    RETURN total;
END;
//
DELIMITER ;

