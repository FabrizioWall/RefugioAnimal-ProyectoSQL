CREATE FUNCTION `cuenta_tratamiento` (animal_id INT) 
RETURNS INT
READS SQL DATA
BEGIN
    DECLARE resultado INT;
    
    SELECT COUNT(*) AS cantidad_tratamientos INTO resultado
    FROM tratamientos
    WHERE id_animal = animal_id;
    
    RETURN resultado;
END;
