DELIMITER //
CREATE TRIGGER before_update_animales
BEFORE UPDATE ON animales
FOR EACH ROW
BEGIN
    INSERT INTO before_logs_animales_ests (id_animal, id_estado, usuario, fecha_modificacion, hora_modificacion)
    VALUES (OLD.id_animal, OLD.id_estado, USER(), CURDATE(), CURTIME());
END;
