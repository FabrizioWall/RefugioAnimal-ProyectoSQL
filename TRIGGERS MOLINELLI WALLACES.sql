CREATE FUNCTION `actualizar_totales_medicamentos`(medicamento_id INT) 
RETURNS varchar(255) 
DETERMINISTIC
BEGIN
	DECLARE resultado VARCHAR(255);
    
    UPDATE medicamentos
	SET monto_total = ((SELECT hacer_totales_medicamentos (medicamento_id)))
